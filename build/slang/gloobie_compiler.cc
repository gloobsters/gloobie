void parseShaderInputs(std::unordered_map<std::string, std::string> &outSourcePaths, const args::ValueFlagList<std::string> &shaderInputs)
{
    for (const auto &input : shaderInputs)
    {
        size_t colonPos = input.find(':');
        if (colonPos == std::string::npos)
        {
            throw std::runtime_error("Invalid shader input format: " + input);
        }

        std::string moduleName = input.substr(0, colonPos);
        std::string path = input.substr(colonPos + 1);

        outSourcePaths[moduleName] = path;
    }
}

struct ShaderVariant
{
    bool skinned;
};

struct ParsedOutput
{
    std::string moduleName;
    std::string path;
    std::string targetPrefix;
    std::string variantStr;
    struct ShaderVariant variant;
};

void generateShaderVariantFile(ShaderVariant variant, std::string &out)
{
    out.clear();
    out += "export static const bool g_skinning = ";
    out += variant.skinned ? "true" : "false";
    out += ";";
}

void parseShaderOutputs(std::vector<ParsedOutput> &outOutputs, const args::ValueFlagList<std::string> &shaderOutputs)
{
    for (const auto &output : shaderOutputs)
    {
        size_t firstColonPos = output.find(':');
        if (firstColonPos == std::string::npos)
        {
            throw std::runtime_error("Invalid shader output format: " + output);
        }

        size_t secondColonPos = output.find(':', firstColonPos + 1);
        if (secondColonPos == std::string::npos)
        {
            throw std::runtime_error("Invalid shader output format: " + output);
        }

        size_t thirdColonPos = output.find(':', secondColonPos + 1);
        if (thirdColonPos == std::string::npos)
        {
            throw std::runtime_error("Invalid shader output format: " + output);
        }

        std::string moduleName = output.substr(0, firstColonPos);
        std::string target = output.substr(firstColonPos + 1, secondColonPos - firstColonPos - 1);
        std::string variantStr = output.substr(secondColonPos + 1, thirdColonPos - secondColonPos - 1);
        std::string path = output.substr(thirdColonPos + 1);

        bool skinned = false;
        if (variantStr.find("skinned") != std::string::npos)
        {
            skinned = true;
        }

        outOutputs.push_back({moduleName, path, target, variantStr, {skinned}});
    }
}

SpvReflectResult reflectSpirvCode(bool verbose, Slang::ComPtr<slang::IBlob> spirvCode, ReflectResult &result)
{
    SpvReflectShaderModule module;
    auto ret = spvReflectCreateShaderModule2(SPV_REFLECT_MODULE_FLAG_NO_COPY, spirvCode->getBufferSize(), spirvCode->getBufferPointer(), &module);
    if (ret != SPV_REFLECT_RESULT_SUCCESS)
    {
        std::cerr << "Failed to create SPIR-V reflection module: " << ret << std::endl;
        return ret;
    }

    // TODO: support the compute stage

    if (verbose)
    {
        std::cerr << "SPIR-V Reflection:" << std::endl;
        std::cerr << "Descriptor Sets: " << module.descriptor_set_count << std::endl;
    }
    for (uint32_t i = 0; i < module.descriptor_set_count; i++)
    {
        const SpvReflectDescriptorSet &set = module.descriptor_sets[i];

        if (verbose)
        {
            std::cerr << "  Set " << set.set << " has " << set.binding_count << " bindings." << std::endl;
        }
        for (uint32_t j = 0; j < set.binding_count; j++)
        {
            const SpvReflectDescriptorBinding &binding = *set.bindings[j];
            assert(set.set < 4);

            struct StageReflectResult &stageResult = (set.set == 0 || set.set == 1) ? result.vertex : result.fragment;
            switch (binding.descriptor_type)
            {
            case SPV_REFLECT_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER:
                stageResult.numSamplers++;
                break;
            case SPV_REFLECT_DESCRIPTOR_TYPE_STORAGE_BUFFER:
                stageResult.numStorageBuffers++;
                break;
            case SPV_REFLECT_DESCRIPTOR_TYPE_UNIFORM_BUFFER:
                stageResult.numUniformBuffers++;
                break;
            default:
                std::cerr << "Unhandled descriptor type: " << binding.descriptor_type << std::endl;
                break;
            }

            if (verbose)
            {
                std::cerr << "    Binding " << binding.binding << ": " << binding.name << " (type: " << binding.descriptor_type << ", count: " << binding.count << ")" << std::endl;
            }
        }
    }

    spvReflectDestroyShaderModule(&module);

    return SPV_REFLECT_RESULT_SUCCESS;
}

struct ParsedReflectOutput
{
    std::string moduleName;
    std::string path;
};

void parseReflectOutputs(std::vector<ParsedReflectOutput> &outOutputs, const args::ValueFlagList<std::string> &reflectOutputs)
{
    for (const auto &output : reflectOutputs)
    {
        size_t colonPos = output.find(':');
        if (colonPos == std::string::npos)
        {
            throw std::runtime_error("Invalid shader reflection output format: " + output);
        }

        std::string moduleName = output.substr(0, colonPos);
        std::string path = output.substr(colonPos + 1);

        outOutputs.push_back({moduleName, path});
    }
}

int main(int argc, char **argv)
{
    args::ArgumentParser parser("Gloobie's shader compiler.");
    args::HelpFlag help(parser, "help", "Display this help menu", {'h', "help"});
    args::Flag verbose(parser, "verbose", "Enable verbose output", {'v', "verbose"});
    args::ValueFlagList<std::string> shaderInputs(parser, "inputs", "The shader inputs, in the format of module_name:input_path", {'i'});
    args::ValueFlagList<std::string> shaderOutputs(parser, "outputs", "The shader outputs, in the format of module_name:target:output_path", {'o'});
    args::ValueFlagList<std::string> reflectOutputs(parser, "reflects", "The shader reflection data outputs, in the format of module_name:output_path", {'r'});
    args::ValueFlag<int> optimizationLevel(parser, "opt", "The optimization level (0-3)", {'O'});
    args::ValueFlag<std::string> debugLevel(parser, "debug", "The debug level", {'g'});
    args::ValueFlagList<std::string> extraLoadPaths(parser, "dll-path", "An extra path to add to the DLL search path.", {'d'});
    args::CompletionFlag completion(parser, {"complete"});
    try
    {
        parser.ParseCLI(argc, argv);
    }
    catch (const args::Completion &e)
    {
        std::cerr << e.what();
        return 0;
    }
    catch (const args::Help &)
    {
        std::cerr << parser;
        return 0;
    }
    catch (const args::ParseError &e)
    {
        std::cerr << e.what() << std::endl;
        std::cerr << parser;
        return 1;
    }
    if (optimizationLevel)
    {
        int opt = optimizationLevel.Get();
        if (opt < 0 || opt > 3)
        {
            std::cerr << "Invalid optimization level: " << opt << std::endl;
            return 1;
        }

        options.push_back(slang::CompilerOptionEntry{slang::CompilerOptionName::Optimization, {slang::CompilerOptionValueKind::Int, opt, 0, nullptr, nullptr}});
    }
    if (debugLevel)
    {
        std::string dbg = debugLevel.Get();
        SlangDebugInfoLevel debugInfoLevel = SLANG_DEBUG_INFO_LEVEL_NONE;
        if (dbg == "none")
        {
            debugInfoLevel = SLANG_DEBUG_INFO_LEVEL_NONE;
        }
        else if (dbg == "minimal")
        {
            debugInfoLevel = SLANG_DEBUG_INFO_LEVEL_MINIMAL;
        }
        else if (dbg == "standard")
        {
            debugInfoLevel = SLANG_DEBUG_INFO_LEVEL_STANDARD;
        }
        else if (dbg == "maximal")
        {
            debugInfoLevel = SLANG_DEBUG_INFO_LEVEL_MAXIMAL;
        }
        else
        {
            std::cerr << "Invalid debug level: " << dbg << std::endl;
            return 1;
        }

        options.push_back(slang::CompilerOptionEntry{slang::CompilerOptionName::DebugInformation, {slang::CompilerOptionValueKind::Int, (int)debugInfoLevel, 0, nullptr, nullptr}});
    }

    for (auto &path : extraLoadPaths)
    {
#ifdef _WIN32
        SetDllDirectoryA(path.c_str());
#endif
    }

    // Pares out the module sources
    std::unordered_map<std::string, std::string> moduleSourcePaths;
    parseShaderInputs(moduleSourcePaths, shaderInputs);

    // Parse out the outputs
    std::vector<ParsedOutput> parsedOutputs;
    parseShaderOutputs(parsedOutputs, shaderOutputs);

    // Parse out the reflection outputs
    std::vector<ParsedReflectOutput> parsedReflectOutputs;
    parseReflectOutputs(parsedReflectOutputs, reflectOutputs);

    Slang::ComPtr<slang::IGlobalSession> globalSession;
    createGlobalSession(globalSession.writeRef());

    globalSession->setDownstreamCompilerPath(SlangPassThrough::SLANG_PASS_THROUGH_GLSLANG, "");

    slang::SessionDesc sessionDesc = {};

    std::vector<slang::TargetDesc> targetDescs(targets.size());
    {
        auto targetIter = targets.begin();

        for (size_t i = 0; targetIter != targets.end(); i++, targetIter++)
        {
            const Target &target = targetIter->second;
            slang::TargetDesc &targetDesc = targetDescs[i];

            targetDesc.format = target.format;
            targetDesc.profile = globalSession->findProfile(target.profileName.c_str());
            targetDesc.compilerOptionEntries = options.data();
            targetDesc.compilerOptionEntryCount = options.size();
        }

        sessionDesc.targets = targetDescs.data();
        sessionDesc.targetCount = targetDescs.size();
    }

    int spirvTargetIndex = -1;
    for (size_t i = 0; i < targetDescs.size(); i++)
    {
        if (targetDescs[i].format == SLANG_SPIRV)
        {
            spirvTargetIndex = (int)i;
            break;
        }
    }
    if (spirvTargetIndex == -1)
    {
        std::cerr << "SPIR-V target not specified. At least one target must be SPIR-V." << std::endl;
        return 1;
    }

    sessionDesc.compilerOptionEntries = options.data();
    sessionDesc.compilerOptionEntryCount = options.size();

    sessionDesc.defaultMatrixLayoutMode = SLANG_MATRIX_LAYOUT_COLUMN_MAJOR;
    sessionDesc.allowGLSLSyntax = true;

    Slang::ComPtr<slang::ISession> session;
    globalSession->createSession(sessionDesc, session.writeRef());

    std::unordered_map<std::string, Slang::ComPtr<slang::IModule>> loadedModules;
    for (auto &sourcePathPair : moduleSourcePaths)
    {
        const std::string &name = sourcePathPair.first;
        const std::string &path = sourcePathPair.second;

        // Read the entire file into a blob
        std::string sourceCode;
        readFileToString(path, sourceCode);

        Slang::ComPtr<slang::IBlob> diagnosticsBlob;
        auto module = loadedModules[name] = session->loadModuleFromSourceString(name.c_str(), path.c_str(), sourceCode.c_str(), diagnosticsBlob.writeRef());

        diagnoseIfNeeded(diagnosticsBlob);
        if (!module)
        {
            std::cerr << "Error loading module: " << name << std::endl;
            return -1;
        }

        if (verbose)
        {
            std::cerr << "Loaded module: " << name << std::endl;
        }
    }

    for (size_t i = 0; i < parsedOutputs.size(); i++)
    {
        ParsedOutput &parsedOutput = parsedOutputs[i];

        // find shader source from module name
        auto shaderSrcIter = std::find_if(shaderSources.begin(), shaderSources.end(), [&](const ShaderSource &src)
                                          { return src.moduleName == parsedOutput.moduleName; });
        if (shaderSrcIter == shaderSources.end())
        {
            std::cerr << "No shader source found for module: " << parsedOutput.moduleName << std::endl;
            return -1;
        }
        const ShaderSource &shaderSrc = *shaderSrcIter;

        Slang::ComPtr<slang::IModule> slangModule = loadedModules[parsedOutput.moduleName];

        std::vector<std::string> entryPointNames;
        std::vector<Slang::ComPtr<slang::IEntryPoint>> entryPoints;
        for (auto &entryPointName : shaderSrc.entryPoints)
        {
            SlangResult result = slangModule->findEntryPointByName(
                entryPointName.c_str(),
                entryPoints.emplace_back().writeRef());
            if (SLANG_FAILED(result))
            {
                std::cerr << "Error finding entry point: " << entryPointName << " for shader " << shaderSrc.moduleName << ", Got result: " << result << std::endl;
                SLANG_RETURN_ON_FAIL(result);
            }
            entryPointNames.push_back(entryPointName);
        }

        std::string variantSource;
        generateShaderVariantFile(parsedOutput.variant, variantSource);

        std::string variantSourcePath = parsedOutput.moduleName + ".variant." + parsedOutput.variantStr;

        Slang::ComPtr<slang::IBlob> diagnosticsBlob;
        slang::IModule *variantModule = session->loadModuleFromSourceString(variantSourcePath.c_str(), (variantSourcePath + ".slang").c_str(), variantSource.c_str(), nullptr);
        diagnoseIfNeeded(diagnosticsBlob);

        if (verbose)
        {
            std::cerr << "Using variant for module " << parsedOutput.moduleName << ": " << std::endl;
            std::cerr << "Variant source path: " << variantSourcePath << std::endl;
            std::cerr << variantSource << std::endl;
        }

        std::vector<slang::IComponentType *>
            componentTypes =
                {
                    slangModule,
                    variantModule,
                };
        for (auto &entryPoint : entryPoints)
        {
            componentTypes.push_back(entryPoint);
        }

        Slang::ComPtr<slang::IComponentType> composedProgram;
        {
            Slang::ComPtr<slang::IBlob> diagnosticsBlob;
            SlangResult result = session->createCompositeComponentType(
                componentTypes.data(),
                componentTypes.size(),
                composedProgram.writeRef(),
                diagnosticsBlob.writeRef());
            diagnoseIfNeeded(diagnosticsBlob);
            SLANG_RETURN_ON_FAIL(result);
        }

        Slang::ComPtr<slang::IComponentType> linkedProgram;
        {
            Slang::ComPtr<slang::IBlob> diagnosticsBlob;
            SlangResult result = composedProgram->link(
                linkedProgram.writeRef(),
                diagnosticsBlob.writeRef());
            diagnoseIfNeeded(diagnosticsBlob);
            SLANG_RETURN_ON_FAIL(result);
        }

        {
            Slang::ComPtr<slang::IBlob> diagnosticsBlob;
            Slang::ComPtr<slang::IBlob> spirvCode;
            auto result = linkedProgram->getTargetCode(spirvTargetIndex, spirvCode.writeRef(), diagnosticsBlob.writeRef());
            diagnoseIfNeeded(diagnosticsBlob);
            SLANG_RETURN_ON_FAIL(result);

            ReflectResult reflectResult{};
            auto spirvResult = reflectSpirvCode(verbose, spirvCode, reflectResult);
            if (spirvResult != SPV_REFLECT_RESULT_SUCCESS)
            {
                std::cerr << "Failed to reflect SPIR-V code: " << spirvResult << std::endl;
                return -1;
            }

            // Write out the reflection result if needed
            auto reflectOutputIter = std::find_if(parsedReflectOutputs.begin(), parsedReflectOutputs.end(), [&](const ParsedReflectOutput &output)
                                                  { return output.moduleName == shaderSrc.moduleName; });
            if (reflectOutputIter != parsedReflectOutputs.end())
            {
                writeReflectToZigFile(reflectOutputIter->path, reflectResult);
            }

            if (verbose)
            {
                std::cerr << "Reflection result for module " << shaderSrc.moduleName << ":" << std::endl;
                std::cerr << "  Vertex Stage:" << std::endl;
                std::cerr << "    Samplers: " << reflectResult.vertex.numSamplers << std::endl;
                std::cerr << "    Uniform Buffers: " << reflectResult.vertex.numUniformBuffers << std::endl;
                std::cerr << "    Storage Textures: " << reflectResult.vertex.numStorageTextures << std::endl;
                std::cerr << "    Storage Buffers: " << reflectResult.vertex.numStorageBuffers << std::endl;
                std::cerr << "  Fragment Stage:" << std::endl;
                std::cerr << "    Samplers: " << reflectResult.fragment.numSamplers << std::endl;
                std::cerr << "    Uniform Buffers: " << reflectResult.fragment.numUniformBuffers << std::endl;
                std::cerr << "    Storage Textures: " << reflectResult.fragment.numStorageTextures << std::endl;
                std::cerr << "    Storage Buffers: " << reflectResult.fragment.numStorageBuffers << std::endl;
                std::cerr << "  Compute Stage:" << std::endl;
                std::cerr << "    Samplers: " << reflectResult.compute.numSamplers << std::endl;
                std::cerr << "    Readonly Storage Textures: " << reflectResult.compute.numReadonlyStorageTextures << std::endl;
                std::cerr << "    Readonly Storage Buffers: " << reflectResult.compute.numReadonlyStorageBuffers << std::endl;
                std::cerr << "    Readwrite Storage Textures: " << reflectResult.compute.numReadwriteStorageTextures << std::endl;
                std::cerr << "    Readwrite Storage Buffers: " << reflectResult.compute.numReadwriteStorageBuffers << std::endl;
                std::cerr << "    Uniform Buffers: " << reflectResult.compute.numUniformBuffers << std::endl;
                std::cerr << "    Thread Count X: " << reflectResult.compute.threadcountX << std::endl;
                std::cerr << "    Thread Count Y: " << reflectResult.compute.threadcountY << std::endl;
                std::cerr << "    Thread Count Z: " << reflectResult.compute.threadcountZ << std::endl;
            }
        }

        std::unordered_map<std::string, Slang::ComPtr<slang::IBlob>>
            finalCode(targets.size());
        for (auto &targetPair : targets)
        {
            const std::string &prefix = targetPair.first;
            const Target &target = targetPair.second;

            // try to find a parsed output that matches this module and target, if not found, just skip
            auto parsedOutputIter = std::find_if(parsedOutputs.begin(), parsedOutputs.end(), [&](const ParsedOutput &output)
                                                 { return output.moduleName == shaderSrc.moduleName && output.targetPrefix == prefix; });
            if (parsedOutputIter == parsedOutputs.end())
            {
                if (verbose)
                {
                    std::cerr << "Skipping output for module " << shaderSrc.moduleName << " target " << prefix << " as no output specified" << std::endl;
                }
                continue;
            }

            // get the target index from the TargetDescs
            int targetIndex = -1;
            for (size_t ti = 0; ti < targetDescs.size(); ti++)
            {
                if (targetDescs[ti].format == target.format)
                {
                    targetIndex = (int)ti;
                    break;
                }
            }
            if (targetIndex == -1)
            {
                throw std::runtime_error("Failed to find target index for target: " + prefix);
                return -1;
            }

            Slang::ComPtr<slang::IBlob> diagnosticsBlob;
            SlangResult result = linkedProgram->getTargetCode(
                targetIndex,
                finalCode[prefix].writeRef(),
                diagnosticsBlob.writeRef());
            diagnoseIfNeeded(diagnosticsBlob);
            SLANG_RETURN_ON_FAIL(result);

            if (verbose)
            {
                std::cerr << "Compiled entry points: " << std::endl;
                for (auto &entryPoint : entryPointNames)
                {
                    std::cerr << "  " << entryPoint << std::endl;
                }
                std::cerr << "for module " << shaderSrc.moduleName << " target " << prefix << std::endl
                          << std::endl;
            }
        }

        auto codeIter = finalCode.find(parsedOutput.targetPrefix);
        if (codeIter == finalCode.end())
        {
            std::cerr << "No compiled code for module " << shaderSrc.moduleName << " target " << parsedOutput.targetPrefix << std::endl;
            continue;
        }

        writeBlobToFileTruncate(parsedOutput.path, codeIter->second);
        if (verbose)
        {
            std::cerr << "Wrote output for module " << shaderSrc.moduleName << " target " << parsedOutput.targetPrefix << " to " << parsedOutput.path << std::endl;
        }
    }

    return 0;
}
