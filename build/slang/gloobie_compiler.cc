void readFileToString(const std::string &path, std::string &out)
{
    std::ifstream file(path);
    if (!file.is_open())
    {
        throw std::runtime_error("Failed to open file: " + path);
    }

    out.assign((std::istreambuf_iterator<char>(file)), std::istreambuf_iterator<char>());
}

void writeBlobToFileTruncate(const std::string &path, slang::IBlob *blob)
{
    std::ofstream file(path, std::ios::binary | std::ios::trunc | std::ios::out);
    if (!file.is_open())
    {
        throw std::runtime_error("Failed to open file for writing: " + path);
    }

    file.write((const char *)blob->getBufferPointer(), blob->getBufferSize());
}

void writeReflectToZigFile(const std::string &path, const ReflectResult &result)
{
    std::ofstream file(path, std::ios::trunc | std::ios::out);
    if (!file.is_open())
    {
        throw std::runtime_error("Failed to open file for writing: " + path);
    }

    file << "///! This file is generated by the Gloobie shader compiler. Do not edit." << std::endl;
    file << std::endl;
    file << "const reflection = @import(\"reflection\");" << std::endl;
    file << "const options = @import(\"options\").build_options;" << std::endl;
    file << std::endl;
    file << "pub const basic: reflection.Shader = .{" << std::endl;
    file << "    .spirv = if (options.render_backends.vulkan) @embedFile(\"spirv\") else &.{}," << std::endl;
    file << "    .dxil = if (options.render_backends.d3d12) @embedFile(\"dxil\") else &.{}," << std::endl;
    file << "    .metal = if (options.render_backends.metal) @embedFile(\"metal\") else &.{}," << std::endl;
    file << std::endl;
    file << "    .vertex_stage = .{" << std::endl;
    file << "        .num_samplers = " << result.vertex.numSamplers << "," << std::endl;
    file << "        .num_uniform_buffers = " << result.vertex.numUniformBuffers << "," << std::endl;
    file << "        .num_storage_textures = " << result.vertex.numStorageTextures << "," << std::endl;
    file << "        .num_storage_buffers = " << result.vertex.numStorageBuffers << "," << std::endl;
    file << "    }," << std::endl;
    file << "    .fragment_stage = .{" << std::endl;
    file << "        .num_samplers = " << result.fragment.numSamplers << "," << std::endl;
    file << "        .num_uniform_buffers = " << result.fragment.numUniformBuffers << "," << std::endl;
    file << "        .num_storage_textures = " << result.fragment.numStorageTextures << "," << std::endl;
    file << "        .num_storage_buffers = " << result.fragment.numStorageBuffers << "," << std::endl;
    file << "    }," << std::endl;
    file << "    .compute_stage = .{" << std::endl;
    file << "        .num_samplers = " << result.compute.numSamplers << "," << std::endl;
    file << "        .num_readonly_storage_textures = " << result.compute.numReadonlyStorageTextures << "," << std::endl;
    file << "        .num_readonly_storage_buffers = " << result.compute.numReadonlyStorageBuffers << "," << std::endl;
    file << "        .num_readwrite_storage_textures = " << result.compute.numReadwriteStorageTextures << "," << std::endl;
    file << "        .num_readwrite_storage_buffers = " << result.compute.numReadwriteStorageBuffers << "," << std::endl;
    file << "        .num_uniform_buffers = " << result.compute.numUniformBuffers << "," << std::endl;
    file << "        .threadcount_x = " << result.compute.threadcountX << "," << std::endl;
    file << "        .threadcount_y = " << result.compute.threadcountY << "," << std::endl;
    file << "        .threadcount_z = " << result.compute.threadcountZ << "," << std::endl;
    file << "    }," << std::endl;
    file << "};" << std::endl;
    file << std::endl;

    file.flush();
    file.close();
}

void diagnoseIfNeeded(slang::IBlob *diagnosticsBlob)
{
    if (diagnosticsBlob)
    {
        const char *diagnosticsPtr = (const char *)diagnosticsBlob->getBufferPointer();
        size_t diagnosticsSize = diagnosticsBlob->getBufferSize();

        if (diagnosticsPtr && diagnosticsSize)
        {
            std::string diagnostics;
            diagnostics.assign(diagnosticsPtr, diagnosticsSize);

            std::cerr << diagnostics << std::endl;
        }
    }
}

